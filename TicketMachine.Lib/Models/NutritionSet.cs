using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TicketMachine.Lib.Models
{
    public class NutritionSet
    {
         public NutritionSet(String aName, Double aNutrition)
        {
            Name = aName;
            nutrition = aNutrition;
        }

        private string name;
        public string Name
        {
            get
            {
                return name;
            }
            set
            {
                name = value;
            }
        }

        private Double nutrition;
        public Double Nutrition
        {
            get
            {
                return nutrition;
            }
            set
            {
                nutrition = value;
            }
        }
    }
}
